package com.xploreict.blooddonation;

public class myprofile_response_model {
    String name,imageurl,mobile,city,bloodgroup,gender,age,id;

    public myprofile_response_model(String name, String imageurl, String mobile, String city, String bloodgroup, String gender, String age, String id) {
        this.name = name;
        this.imageurl = imageurl;
        this.mobile = mobile;
        this.city = city;
        this.bloodgroup = bloodgroup;
        this.gender = gender;
        this.age = age;
        this.id = id;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImageurl() {
        return imageurl;
    }

    public void setImageurl(String imageurl) {
        this.imageurl = imageurl;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getBloodgroup() {
        return bloodgroup;
    }

    public void setBloodgroup(String bloodgroup) {
        this.bloodgroup = bloodgroup;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }
}
